package JavaBasics;

public class comparison_operators {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;

        System.out.println(x == y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x != y);

        // we use Class.equals for reference types to see if they equal each other.

    }
}
